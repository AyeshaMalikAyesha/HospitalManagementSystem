    private void updateJBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateJBActionPerformed
        // TODO add your handling code here:
        try{
        String name=nameJF.getText();
        String admitDate=admitDateJField.getText();
        String disease=diseaseJField.getText();
        String age=ageJF.getText();
        String id=idJF.getText();
        String gender=null;
        if(maleJRB.isSelected()){
            gender="male";
        }
        else if(femaleJRB.isSelected()){
            gender="female";
        }
        if(otherJRB.isSelected()){
            gender="other";
        }
        if(name.equals("")||admitDate.equals("")||disease.equals("")||age.equals("")||id.equals("")||gender.equals("")){
            throw new NullPointerException();
        }
        else{
            try{
            int a=Integer.parseInt(age);
        int i=Integer.parseInt(id);
        Patients p=new Patients(name,gender,disease,admitDate,i,a);
        f.update(p);
        JOptionPane.showMessageDialog(this, "UPDATED SUCCESSFULLY","***CONFIRMATION***",JOptionPane.INFORMATION_MESSAGE);
        nameJF.setText("");
        admitDateJField.setText("");
        diseaseJField.setText("");
        ageJF.setText("");
        idJF.setText("");
        maleJRB.setSelected(false);
        femaleJRB.setSelected(false);
        otherJRB.setSelected(false);
        patientIdJField.setText("");
            }
            catch(NumberFormatException e){
                JOptionPane.showMessageDialog(this, "****NUMBER FORMAT EXCEPTION, ****ENTER ANY INTEGER IN FIELD AGE****","**INVALID**",JOptionPane.ERROR_MESSAGE);
            }
        }
        }
        catch(NullPointerException e){
             JOptionPane.showMessageDialog(this, "****NULL POINTER EXCEPTION,FILL DOCTOR ID FIELD****","**INVALID**",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_updateJBActionPerformed

