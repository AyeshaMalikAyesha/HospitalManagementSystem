    private void okJBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_okJBActionPerformed
        // TODO add your handling code here:
        boolean check=false;
        idJF.setEditable(false);
        try{
        
        String id=patientIdJField.getText();
        if(id.equals("")){
            throw new NullPointerException();
        }
        else{
            try{
        int idd=Integer.parseInt(id);
        s=f.getArray();
        for(int i=0;i<s.size();i++){
            Patients p=s.get(i);
            if(p.getId()==(idd)){
                check=true;
                nameJF.setText(p.getName());
                admitDateJField.setText(p.getAdmitDate());
                diseaseJField.setText(p.getDisease());
                idJF.setText(p.getId()+"");
                ageJF.setText(p.getAge()+"");
                String gender=p.getGender();
                if(gender.equalsIgnoreCase("MALE")){
                    maleJRB.setSelected(true);
                }
                else if(gender.equalsIgnoreCase("FEMALE")){
                    femaleJRB.setSelected(true);
                }
                else if(gender.equalsIgnoreCase("OTHER")){
                    otherJRB.setSelected(true);
                }
                
            }
        }
         if(check==true){
            JOptionPane.showMessageDialog(this, "****RECORD IS FOUND SUCCESSFULLY****","***CONFIRMATION***",JOptionPane.INFORMATION_MESSAGE);
        }
        else if(check==false){
            JOptionPane.showMessageDialog(this, "****RECORD IS NOT FOUND!!!!","***INVALID***",JOptionPane.ERROR_MESSAGE);
        }
        }
             catch(NumberFormatException e){
            JOptionPane.showMessageDialog(this, "NUMBER FORMAT EXCEPTION OCCURED ENTER INTEGER IN FIELD ID","***INVALID***",JOptionPane.ERROR_MESSAGE);
        }
        }
        }
        catch(NullPointerException e){
            JOptionPane.showMessageDialog(this, "NULL POINTER EXCEPTION OCCURED,PATIENT ID FIELD IS EMPTY, ENTER ID IN FIELD ID","***INVALID***",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_okJBActionPerformed

